# Logical operators

#Checking for logical condintions

"""
and (Checks for both True Statements)
or (Checks for at least one True condition)
not 

"""

print(26<25 and 23>12) #False
print(2<25 and 23>3)  #True
print(35<25 or 23>3)  #True
print(33<25 or 23>36)  #False
print(not(23>56)) #True
print(not(True)) #False
print(not(False)) #True